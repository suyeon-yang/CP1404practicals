import random
print(random.randint(5, 20))  # line 1
# Number was 13
# The smallest number possible should be 5 and largest should be 20
print(random.randrange(3, 10, 2))  # line 2
# Number was 5
# The smallest number possible should be 3 and largest should be 9
# 4 cannot be produced for numbers are produced in a range of 2
print(random.uniform(2.5, 5.5))  # line 3
# Number was 3.1620363619551015
# The smallest number possible should be 2.5 and largest should be 5.5

